// Q1
let choice = 2;
switch (choice) {
  case 1: {
    console.log("Tea");
    break;
  }
  case 2: {
    console.log("Coffee");
    break;
  }
  case 3: {
    console.log("Regular Tea");
    break;
  }
  default: {
    console.log("Water");
  }
}

